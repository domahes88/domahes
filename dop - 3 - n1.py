t = int(input('Введите первое число '))
t2 = int(input('Введите второе число '))
f = input()
if f == '+':
    print(t+t2)
if f == '-':
    print(t-t2)
if f == '*':
    print(t*t2)
if f == '**':
    print(t**t2)
if f == '/':
    print(t/t2)
if f == '//':
    print(t//t2)
if f == '%':
    print(t%t2)
